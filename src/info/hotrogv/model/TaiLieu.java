package info.hotrogv.model;

public class TaiLieu {
	private int taiLieuID;
	private String tieuDe;
	private String moTa;
	private String file;
	private int idBaiGiang;
	
	
	public int getTaiLieuID() {
		return taiLieuID;
	}
	public void setTaiLieuID(int taiLieuID) {
		this.taiLieuID = taiLieuID;
	}
	public String getTieuDe() {
		return tieuDe;
	}
	public void setTieuDe(String tieuDe) {
		this.tieuDe = tieuDe;
	}
	public String getMoTa() {
		return moTa;
	}
	public void setMoTa(String moTa) {
		this.moTa = moTa;
	}
	public String getFile() {
		return file;
	}
	public void setFile(String file) {
		this.file = file;
	}
	public int getIdBaiGiang() {
		return idBaiGiang;
	}
	public void setIdBaiGiang(int idBaiGiang) {
		this.idBaiGiang = idBaiGiang;
	}
	
}
